package day08_relational_operators;

public class OperatorPractice2 {
    public static void main(String[] args) {
        int a = 40; // a = 40 --> 39
        System.out.println(--a); // predecrement , subtract 1 right away and prints value of a which is 39
        System.out.println(--a); // predecrement , subtract 1 right away and prints value of a which is 38
        System.out.println(a--); //postdecrement , first get the value of a which is 38
        System.out.println(--a);
        System.out.println(--a);
        System.out.println(a--);












    }

















}




