package day05_variables;

public class MyName {
    public static void main(String[] args) {

        char first = 'a';
        char second = 'b';
        char third = 'u';
        char fourth = 'z';
        char fifth = 'a';
        char sixth = 'r';


        System.out.println(first);
        System.out.println(second);
        System.out.println(third);
        System.out.println(fourth);
        System.out.println(fifth);
        System.out.println(sixth);


        System.out.println("My Name:" + first + second + third + fourth + fifth + sixth);

        // or you can make the variable of your name

        String name = "" + first + second + third + fourth + fifth + sixth ; // string name = abuzar
        System.out.println("My Name:" + name);















    }

















}
